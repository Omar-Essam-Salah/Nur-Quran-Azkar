export const getAzkarCategoryById = (id: string): AzkarCategory | undefined => {
  return azkarCategories.find(cat => cat.id === id);
};

export const getAzkarItemById = (categoryId: string, itemId: string) => {
  const category = getAzkarCategoryById(categoryId);
  return category?.items.find(item => item.id === itemId);
};
